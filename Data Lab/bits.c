/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 */
int leastBitPos(int x) {
  //by taking the complement of x, we switch the positions of the ones and zeros- when we add one, the 1 will carry until it lands in the spot in which there is only a 0, i.e. where there is a one in the original number
  return ((~x + 1) & x);
}
/* 
 * greatestBitPos - return a mask that marks the position of the
 *               most significant 1 bit. If x == 0, return 0
 *   Example: greatestBitPos(96) = 0x40
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 70
 */
int greatestBitPos(int x) {
  // the following operations with x allow us to find the position of the first significatn 1 bit and turn the value of x into a number with zeros, followed by the first significant 1 bit, followed by 1s until the end, ex. 001010101 becomes 01111111
  x = x | x>>1;
  x = x | x>>2;
  x = x | x>>4;
  x = x | x>>8;
  x = x | x>>16;
  //the last operation extracts the position of the first significant 1 bit by using xor to get rid of all the other 1s besides the first significant. ex. 01111111 becomes 01000000
  x = x & (((x)^(x >> 1)) | (1 << 31));
  return x;
}
