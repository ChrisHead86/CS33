/* 
 * TMax - return maximum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmax(void) {
  //(1 << 31) gives us the biggest possible negative number- by taking the complement, we get the biggest possible positive number
  int x = ~(1 << 31);
  return x;
}
/*
 * isZero - returns 1 if x == 0, and 0 otherwise 
 *   Examples: isZero(5) = 0, isZero(0) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int isZero(int x) {
 //if x is zero, using bang will return 1, if x is not zero, using bang will return zero
 return !x;
}
/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  //the first int takes the complement of x&y, which gives us 1s in the spaces in which neither x or why has a zero
  int xy_comp = ~(x&y);
  //the following statement extracts the positions in which there is only a 1 in x or y
  return ~(~(x&xy_comp)&~(y&xy_comp));
}
/* 
 * isNotEqual - return 0 if x == y, and 1 otherwise 
 *   Examples: isNotEqual(5,5) = 0, isNotEqual(4,5) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNotEqual(int x, int y) {
  //the first int gives us the positions in which the numbers differ
  int a = x^y;
  //we use bang to convert this to a single bit and return it after banging it again
  int b = !a;
  return !b;
}
/* 
 * sign - return 1 if positive, 0 if zero, and -1 if negative
 *  Examples: sign(130) = 1
 *            sign(-23) = -1
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 10
 *  Rating: 2
 */
int sign(int x) {
   //the following line takes the negative of x, shifts it 31 bits to the right, then takes the negative of that-the code adds this to x shifted to the right by 31 bits
   return  (x >> 31) + (~((~x+1) >> 31) + 1);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  int a = !x;
  int b = !a;
  int c = ~b + 1;
  return (c&y)|(~c&z);
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  int a = c << (n << 3);
  int b = x&~(0xff << (n << 3));
  return a^b;
}
/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  //first, we check if the number is negative, then we check if it is between 0x30 and 0x39
  int a  = ~(0x30);
  int b = ~((1 << 31) | 0x39);
  int c;
  a = (1 << 31) & (a + x + 1) >> 31;
  b = (1 << 31) & (b + x) >> 31;
  c = !(a|b);
  return c;
}
/* 
 * subtractionOK - Determine if can compute x-y without overflow
 *   Example: subtractionOK(0x80000000,0x80000000) = 1,
 *            subtractionOK(0x80000000,0x70000000) = 0, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int subtractionOK(int x, int y) {
  //this first int checks if only one of the numbers is negative
  int a = (x >> 31)^(y >> 31);
  //we try subtracting the two numbers- see what we get
  int b = (x + (~y+1)) >> 31;
  int c = a & (b^(x >> 31));
  return !c;
}
